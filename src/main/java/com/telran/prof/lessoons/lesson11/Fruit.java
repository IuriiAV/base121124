package com.telran.prof.lessoons.lesson11;

public class Fruit {

    private String title;

    private int price;

    public Fruit(String title, int price) {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public int getPrice() {
        return price;
    }
}
